function setLang(lang) {
    const safeLang = translations[lang] ? lang : 'de';
    localStorage.setItem('lang', safeLang);
    applyTranslations(safeLang);
}

function applyTranslations(lang) {
    const safeLang = translations[lang] ? lang : 'de';
    document.documentElement.lang = safeLang;
    const dict = translations[safeLang];

    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (dict[key]) {
            el.textContent = dict[key];
        }
    });

    document.querySelectorAll('[data-i18n-html]').forEach(el => {
        const key = el.getAttribute('data-i18n-html');
        if (dict[key]) {
            el.innerHTML = dict[key];
        }
    });
}

function toggleMenu() {
    const menu = document.getElementById('nav-menu');
    const button = document.querySelector('.hamburger');

    if (!menu) return;

    const isOpen = menu.classList.toggle('open');

    if (button) {
        button.setAttribute('aria-expanded', isOpen ? 'true' : 'false');
    }
}

function closeMenu() {
    const menu = document.getElementById('nav-menu');
    const button = document.querySelector('.hamburger');

    if (menu) {
        menu.classList.remove('open');
    }

    if (button) {
        button.setAttribute('aria-expanded', 'false');
    }
}

function acceptCookies() {
    const banner = document.getElementById('cookie-banner');

    if (banner) {
        banner.classList.remove('active');
    }

    localStorage.setItem('cookieConsent', 'accepted');
}

function setActiveNavLink() {
    const currentPath = window.location.pathname.split('/').pop() || 'index.html';
    const navLinks = document.querySelectorAll('#nav-menu a');

    navLinks.forEach(link => {
        const href = link.getAttribute('href');
        if (!href) return;

        const linkPath = href.split('/').pop();

        if (linkPath === currentPath) {
            link.setAttribute('aria-current', 'page');
        } else {
            link.removeAttribute('aria-current');
        }
    });
}

document.addEventListener('DOMContentLoaded', function () {
    const savedLang = localStorage.getItem('lang') || 'de';
    setLang(savedLang);

    const banner = document.getElementById('cookie-banner');
    const acceptBtn = document.getElementById('accept-cookies');
    const navLinks = document.querySelectorAll('#nav-menu a');
    const hamburger = document.querySelector('.hamburger');

    if (banner && !localStorage.getItem('cookieConsent')) {
        banner.classList.add('active');
    }

    if (acceptBtn) {
        acceptBtn.addEventListener('click', acceptCookies);
    }

    if (hamburger && !hamburger.hasAttribute('aria-expanded')) {
        hamburger.setAttribute('aria-expanded', 'false');
    }

    navLinks.forEach(link => {
        link.addEventListener('click', closeMenu);
    });

    setActiveNavLink();
});

function toggleMenu() {
    const menu = document.getElementById('nav-menu');
    if (menu) {
        menu.classList.toggle('open');
    }
}

function initDonationForm() {
    const toggleButtons = document.querySelectorAll('.toggle-btn');
    const amountButtons = document.querySelectorAll('.amount-btn');
    const customAmountBlock = document.getElementById('custom-amount-block');
    const customAmountInput = document.getElementById('custom-amount-input');
    const summaryText = document.getElementById('donation-summary-text');
    const donatePaypal = document.getElementById('donate-paypal');
    const donateCard = document.getElementById('donate-card');

    if (
        !toggleButtons.length ||
        !amountButtons.length ||
        !customAmountBlock ||
        !customAmountInput ||
        !summaryText ||
        !donatePaypal ||
        !donateCard
    ) {
        return;
    }

    let mode = 'once';
    let amount = '10';

    const links = {
        once: {
            paypal: {
                10: 'https://www.paypal.com/donate/?amount=10',
                25: 'https://www.paypal.com/donate/?amount=25',
                50: 'https://www.paypal.com/donate/?amount=50',
                100: 'https://www.paypal.com/donate/?amount=100'
            },
            card: {
                10: 'https://buy.stripe.com/DEIN_LINK_10',
                25: 'https://buy.stripe.com/DEIN_LINK_25',
                50: 'https://buy.stripe.com/DEIN_LINK_50',
                100: 'https://buy.stripe.com/DEIN_LINK_100'
            }
        },
        monthly: {
            paypal: 'https://www.paypal.com/donate/DEIN_MONATLICHER_PAYPAL_LINK',
            card: 'https://buy.stripe.com/DEIN_MONATLICHER_STRIPE_LINK'
        }
    };

    function updateSummary() {
        const modeLabel = mode === 'once' ? 'einmalig' : 'monatlich';
        const customValue = customAmountInput.value.trim();
        let finalAmount = amount;

        if (amount === 'custom' && customValue) {
            finalAmount = customValue;
            summaryText.textContent = `${customValue} € ${modeLabel}`;
        } else if (amount === 'custom') {
            finalAmount = '';
            summaryText.textContent = `Eigener Betrag ${modeLabel}`;
        } else {
            summaryText.textContent = `${amount} € ${modeLabel}`;
        }

        if (mode === 'once') {
            if (finalAmount && links.once.paypal[finalAmount] && links.once.card[finalAmount]) {
                donatePaypal.href = links.once.paypal[finalAmount];
                donateCard.href = links.once.card[finalAmount];
            } else {
                donatePaypal.href = 'https://www.paypal.com/donate/';
                donateCard.href = 'https://buy.stripe.com/DEIN_STANDARD_LINK';
            }
        } else {
            donatePaypal.href = links.monthly.paypal;
            donateCard.href = links.monthly.card;
        }
    }

    toggleButtons.forEach((button) => {
        button.addEventListener('click', () => {
            toggleButtons.forEach((btn) => {
                btn.classList.remove('active');
                btn.setAttribute('aria-selected', 'false');
            });

            button.classList.add('active');
            button.setAttribute('aria-selected', 'true');
            mode = button.dataset.mode;
            updateSummary();
        });
    });

    amountButtons.forEach((button) => {
        button.addEventListener('click', () => {
            amountButtons.forEach((btn) => btn.classList.remove('active'));
            button.classList.add('active');
            amount = button.dataset.amount;

            if (amount === 'custom') {
                customAmountBlock.hidden = false;
                customAmountInput.focus();
            } else {
                customAmountBlock.hidden = true;
                customAmountInput.value = '';
            }

            updateSummary();
        });
    });

    customAmountInput.addEventListener('input', updateSummary);
    updateSummary();
}

document.addEventListener('DOMContentLoaded', () => {
    initDonationForm();
});